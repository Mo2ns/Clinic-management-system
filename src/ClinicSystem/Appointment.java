            Gender = "Male";
        else
            Gender="Female";
        
        String date = txtDate.getText();
        String status ;
        if(rdbAct.isSelected())
            status = "Active";
        else
            status="Finished";
        
        try {
            
            stmt = con.createStatement();
            String sql = "INSERT INTO DB.APPINTMENT (APMTID, PATIENTNAME, GENDER, DATE, STATUS) \n" +
"	VALUES ("+aptId+", '"+patName+"', '"+Gender+"', '"+date+"', '"+status+"')";
        
        stmt.executeUpdate(sql);
        
        readData();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

        

