    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
                try {
            // Connect with DB
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/FinalProjectDB", "db", "123");
            String sql = "UPDATE DB.APPINTMENT SET patientName=?, Gender=?, date=?, status=? WHERE apmtId=?";
            PreparedStatement pst = con.prepareStatement(sql);

            // Fill DB
            pst.setString(1, txtPtName.getText());
            pst.setString(2, rdbMale.isSelected() ? "Male" : "Female");
            pst.setString(3, txtDate.getText()); // تمرير التاريخ كنص أسهل في Derby
            pst.setString(4, rdbAct.isSelected() ? "Active" : "Finished");
            pst.setInt(5, Integer.parseInt(txtAptId.getText()));

            // Execute and update db
            pst.executeUpdate();

            btnRest.doClick(); // لتحديث الجدول فوراً


        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,  e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

