    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked

        int row = tbl.getSelectedRow();
        // get appointment ID from table And set txt false
        txtAptId.setText(tbl.getValueAt(row, 0).toString());
        

        // get patient name from table And set txt false
        txtPtName.setText(tbl.getValueAt(row, 1).toString());
        

        String gender = tbl.getValueAt(row, 2).toString();
        
        if(gender.equalsIgnoreCase("Male")){
            rdbMale.setSelected(true);
        } else {
            rdbFemale.setSelected(true);
        }

        // get Date from table And set txt false
        txtDate.setText(tbl.getValueAt(row, 3).toString());
        

        String status = tbl.getValueAt(row, 4).toString();
        if(status.equalsIgnoreCase("Active")){
            rdbAct.setSelected(true);
        } else {
            rdbFin.setSelected(true);
        }

    }//GEN-LAST:event_tblMouseClicked

